function colors = generateColors()
%
% colors = generateColors()
%
% Description: Helper function to generate colors for plotting.
%
% Arguments:
%     None.
%
% Outputs:
%     colors -- structure containing custom colors
%         reds   -- (1 x 8) cell array; red colors (rgb triplets)
%                   in descending order of darkness.
%         greens -- (1 x 8) cell array; green colors (rgb triplets)
%                   in descending order of darkness.
%         blues  -- (1 x 8) cell array; blue colors (rgb triplets)
%                   in descending order of darkness.
%         grays  -- (1 x 8) cell array; gray colors (rgb triplets)
%                   in descending order of darkness.
%         
% Authors:
%     Evren Gokcen    egokcen@cmu.edu
%
% Revision history:
%     10 Apr 2020 -- Initial full revision.

colors.reds = {[102 0 0] ./ 255,...
               [153 0 0] ./ 255,...
               [204 0 0] ./ 255,...
               [255 0 0] ./ 255,...
               [255 51 51] ./ 255,...
               [255 102 102] ./ 255,...
               [255 153 153] ./ 255,...
               [255 204 204] ./ 255};

colors.greens = {[0 102 0] ./ 255,...
                 [0 153 0] ./ 255,...
                 [0 204 0] ./ 255,...
                 [0 255 0] ./ 255,...
                 [51 255 51] ./ 255,...
                 [102 255 102] ./ 255,...
                 [153 255 153] ./ 255,...
                 [204 255 204] ./ 255};

colors.blues = {[0 51 102] ./ 255,...
                [0 76 153] ./ 255,...
                [0 102 204] ./ 255,...
                [0 128 255] ./ 255,...
                [51 153 255] ./ 255,...
                [102 178 255] ./ 255,...
                [153 204 255] ./ 255,...
                [204 229 255] ./ 255};
            
colors.grays = {[0 0 0], ...
                [0.1 0.1 0.1], ...
                [0.2 0.2 0.2], ...
                [0.3 0.3 0.3], ...
                [0.4 0.4 0.4], ...
                [0.5 0.5 0.5], ...
                [0.6 0.6 0.6], ...
                [0.7 0.7 0.7]};